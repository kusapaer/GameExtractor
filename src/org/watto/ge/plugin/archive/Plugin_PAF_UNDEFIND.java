  @Override
  public int getMatchRating(FileManipulator fm) {
    try {

      int rating = 0;

      if (FieldValidator.checkExtension(fm, extensions)) {
        rating += 25;
      }

      // Header
      if (fm.readString(12).equals("UNDEFINDENDI")) {
        rating += 50;
      }

      fm.skip(58);

      if (fm.readString(4).equals("FSB5")) {
        rating += 5;
      }
      else {
        rating = 0;
      }

      return rating;

    }
    catch (Throwable t) {
      return 0;
    }
  }

  /**
   **********************************************************************************************
   * Reads an [archive] File into the Resources
   **********************************************************************************************
   **/
  @Override
  public Resource[] read(File path) {
    try {

      // NOTE - Compressed files MUST know their DECOMPRESSED LENGTH
      //      - Uncompressed files MUST know their LENGTH

      addFileTypes();

      //ExporterPlugin exporter = Exporter_ZLib.getInstance();

      // RESETTING GLOBAL VARIABLES

      FileManipulator fm = new FileManipulator(path, false);

      // 12 - Header (UNDEFINDENDI)
      // 4 - Unknown (3)
      // 8 - null
      // 4 - Platform Name Length (2)
      // 2 - Platform Name (PC)
      // 4 - Unknown (36)
      // 4 - Unknown (1)
      // 4 - Block Length
      // 4 - Unknown (7)
      // 16 - null
      // 4 - Unknown
      // 4 - Unknown
      fm.skip(70);

      // X - FSB5 Archive
      return new Plugin_FSB_FSB5().read(fm);

    }
    catch (Throwable t) {
      logError(t);
      return null;
    }
  }

