  /**
  **********************************************************************************************
  
  **********************************************************************************************
  **/
  @Override
  public int getMatchRating(FileManipulator fm) {
    try {

      int rating = 0;

      if (FieldValidator.checkExtension(fm, extensions)) {
        rating += 25;
      }
      else {
        return 0; // don't want to match normal FSB4 archives
      }

      // Header
      if (fm.readString(4).equals("FSB4")) {
        rating += 50;
      }

      return rating;

    }
    catch (Throwable t) {
      return 0;
    }
  }

  /**
   **********************************************************************************************
   * Reads an [archive] File into the Resources
   **********************************************************************************************
   **/
  @Override
  public Resource[] read(File path) {
    try {

      // NOTE - Compressed files MUST know their DECOMPRESSED LENGTH
      //      - Uncompressed files MUST know their LENGTH

      addFileTypes();

      //ExporterPlugin exporter = Exporter_ZLib.getInstance();

      // RESETTING GLOBAL VARIABLES

      FileManipulator fm = new FileManipulator(path, false, 128); // small quick reads

      long arcSize = fm.getLength();

      int numFiles = Archive.getMaxFiles();

      Resource[] resources = new Resource[numFiles];
      TaskProgressManager.setMaximum(arcSize);

      // Loop through directory
      int realNumFiles = 0;
      while (fm.getOffset() < arcSize) {
        long offset = fm.getOffset();

        // 4 - Header (FSB4)
        // 4 - Number Of Files
        fm.skip(8);

        // 4 - Details Directory Length
        int detailsLength = fm.readInt();
        FieldValidator.checkLength(detailsLength, arcSize);

        // 4 - File Data Length
        int dataLength = fm.readInt();
        FieldValidator.checkLength(dataLength, arcSize);

        // 2 - Version Major
        // 2 - Version Minor
        // 4 - Mode
        // 8 - MD5 Hash
        // 16 - GUID
        // 2 - Entry Length (80)
        fm.skip(34);

        // 30 - Filename (null terminated)
        String filename = fm.readNullString(30) + ".fsb";

        int length = dataLength + detailsLength + 48;
        FieldValidator.checkLength(length, arcSize);

        fm.relativeSeek(offset + length);

        //path,name,offset,length,decompLength,exporter
        resources[realNumFiles] = new Resource(path, filename, offset, length);

        TaskProgressManager.setValue(offset);
        realNumFiles++;

        // X - null Padding to a multiple of 2048 bytes
        fm.skip(calculatePadding(fm.getOffset(), 2048));
      }

      resources = resizeResources(resources, realNumFiles);

      fm.close();

      return resources;

    }
    catch (Throwable t) {
      logError(t);
      return null;
    }
  }

  /**
   **********************************************************************************************
   * Writes an [archive] File with the contents of the Resources
   **********************************************************************************************
   **/
  @Override
  public void write(Resource[] resources, File path) {
    try {

      FileManipulator fm = new FileManipulator(path, true);
      int numFiles = resources.length;
      TaskProgressManager.setMaximum(numFiles);

      // Write Files
      TaskProgressManager.setMessage(Language.get("Progress_WritingFiles"));
      for (int i = 0; i < numFiles; i++) {
        Resource resource = resources[i];
        write(resource, fm);

        int length = (int) resource.getDecompressedLength();
        int paddingSize = calculatePadding(length, 2048);
        for (int p = 0; p < paddingSize; p++) {
          fm.writeByte(0);
        }

        TaskProgressManager.setValue(i);
      }

      fm.close();

    }
    catch (Throwable t) {
      logError(t);
    }
  }

